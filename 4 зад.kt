        3 -> "Среда"
        4 -> "Четверг"
        5 -> "Пятница"
        6 -> "Суббота"
        7 -> "Воскресенье"
        else -> "Недопустимый номер дня"
    }
    println("День недели для номера $dayNumber: $dayOfWeek")

    // 2. Определить тип треугольника по длинам сторон
    val a = 5
    val b = 5
    val c = 5
    val triangleType = when {
        a == b && b == c -> "Равносторонний треугольник"
        a == b || a == c || b == c -> "Равнобедренный треугольник"
        else -> "Разносторонний треугольник"
    }

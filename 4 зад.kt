        in 18..23, in 0..5 -> "Вечер"
        else -> "Недопустимый час"
    }
    println("Время суток для часа $hour: $timeOfDay")

    // 5. Определить знак числа
    val number = -15
    val sign = when {
        number > 0 -> "Положительное число"
        number < 0 -> "Отрицательное число"
        else -> "Ноль"
    }
    println("Число $number является $sign")

    // 6. Угадай число (пример на усмотрение пользователя)

    // 7. Определение длины строки
    val str = "Пример строки"
    val length = when (str.length) {
        0 -> "Пустая строка"
        in 1..10 -> "Короткая строка"
        in 11..50 -> "Средняя строка"
        else -> "Длинная строка"
    }
    println("Длина строки \"$str\": $length")

    // 8. Определить время приготовления по типу пищи (пример на усмотрение пользователя)

    // 9. Определение длины строки (уже использовано в задаче 7)

    // 10. Способы оплаты
    val paymentMethod = "наличные"
    val paymentMessage = when (paymentMethod) {
        "наличные" -> "Оплата наличными"
        "кредитная карта" -> "Оплата кредитной картой"
        "PayPal" -> "Оплата через PayPal"
        else -> "Неизвестный способ оплаты"
    }
    println("Выбранный способ оплаты \"$paymentMethod\": $paymentMessage")

    // 11. Группа крови
    val bloodType = "AB"
    val transfusionOptions = when (bloodType) {
        "A" -> "Можно переливать группам A и AB"
        "B" -> "Можно переливать группам B и AB"
        "AB" -> "Можно переливать только группе AB"
        "O" -> "Можно переливать всем группам крови"
        else -> "Недопустимая группа крови"
    }
    println("Для группы крови \"$bloodType\": $transfusionOptions")

